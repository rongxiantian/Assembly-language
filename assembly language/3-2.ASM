DATA SEGMENT
NUM DB 30H
WOR DB 30H
OTH DB 30H
DATA ENDS
CODE SEGMENT
ASSUME CS:CODE,DS:DATA
START: MOV AX,DATA
MOV DS,AX 
SSS: MOV AH,1
     INT 21H

CMP AL,13
JZ EXIT
CMP AL,30H
JB OTHS
CMP AL,3AH
JB NUMS 
CMP AL,41H
JB OTHS
CMP AL,5BH
JB WORS
CMP AL,61H
JB OTHS
CMP AL,7AH
JB WORS
OTHS:INC OTH
JMP SSS
NUMS:INC NUM
JMP SSS
WORS:INC WOR
JMP SSS
EXIT:
MOV DL,WOR
     MOV AH,2
     INT 21H
MOV DL,0AH
MOV AH,2
INT 21H
MOV DL,NUM
     MOV AH,2
     INT 21H
MOV DL,0AH
MOV AH,2
INT 21H
MOV DL,OTH
     MOV AH,2
     INT 21H

     MOV AH,4CH
     INT 21H
     RET
CODE ENDS
END START